#Користувач вводить 2 числа, першу та останню цифру кожного числа потрібно поменяти місцями та знайти добуток цих чисел 
arr1 = int(input("Введіть перше число: "))
arr2 = int(input("Введіть друге число: "))

f1 = arr1 // 10
l1 = arr1 % 10

f2 = arr2 // 10
l2 = arr2 % 10

s1 = l1 * 10 + f1
s2 = l2 * 10 + f2

p = s1 * s2
print("Добуток чисел після заміни першої та останньої цифри:", p)
