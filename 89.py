#Користувач вводить 3 числа, потрібно їх обьєднати, перевернути та результат розділити на 4  
arr1 = int(input("Введіть перше число: "))
arr2 = int(input("Введіть друге число: "))
arr3 = int(input("Введіть третє число: "))

c1 = str(arr1) + str(arr2) + str(arr3)
c2 = c1[::-1]
res = int(c2) / 4
print("Результат реверсу та поділу на 4:", res)
